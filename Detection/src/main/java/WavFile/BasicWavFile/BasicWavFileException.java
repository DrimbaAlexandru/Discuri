package WavFile.BasicWavFile;

public class BasicWavFileException extends Exception
{
	public BasicWavFileException()
	{
		super();
	}

	public BasicWavFileException(String message)
	{
		super(message);
	}

	public BasicWavFileException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public BasicWavFileException(Throwable cause)
	{
		super(cause);
	}
}
